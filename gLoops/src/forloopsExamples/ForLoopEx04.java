package forloopsExamples;

public class ForLoopEx04 {
	public static void main(String[] args) {
		for(int a=1; a<=5; a++,System.out.println("Hello")) 
		{
			System.out.println("Rahul");
		}
		System.out.println("-----------------");
		int a=1;
		for(System.out.println("Hi"); a<=5; a++,System.out.println("Hello")) 
		{
			System.out.println("Rahul");
		}
	}
}
