package incrementdecrement;

public class IncrementDecrementOperatorEx2 {

	public static void main(String[] args) {
		int a=10;
		a++;//postfix
		System.out.println(a);
		
		int b=10;
		++b;//prefix
		System.out.println(b);
		
		int c=10;
		c--;
		System.out.println(c);
		
		int d=10;
		--d;
		System.out.println(d);
	}

}
